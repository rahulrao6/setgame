package cs3500.set.model.hw02;

/**
 * Represents how many objects are on each particular card.
 */
public enum Count {


  ONE {
    @Override
    public String toString() {
      return "1";
    }
  },
  TWO {
    @Override
    public String toString() {
      return "2";
    }
  },
  THREE {
    @Override
    public String toString() {
      return "3";
    }
  }


}





